package edu.upf.nets.mercury.pojo;


public class Hop {
	
	private String id;
	private String ip;
	private String ns;
	private String rtt1;
	private String rtt2;
	private String rtt3;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getNs() {
		return ns;
	}
	public void setNs(String ns) {
		this.ns = ns;
	}
	public String getRtt1() {
		return rtt1;
	}
	public void setRtt1(String rtt1) {
		this.rtt1 = rtt1;
	}
	public String getRtt2() {
		return rtt2;
	}
	public void setRtt2(String rtt2) {
		this.rtt2 = rtt2;
	}
	public String getRtt3() {
		return rtt3;
	}
	public void setRtt3(String rtt3) {
		this.rtt3 = rtt3;
	}
	
	
}
